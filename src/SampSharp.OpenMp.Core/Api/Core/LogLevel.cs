namespace SampSharp.OpenMp.Core.Api;

/// <summary>
/// Represents the severity level of a log message.
/// </summary>
public enum LogLevel
{
    /// <summary>
    /// A debug message.
    /// </summary>
    Debug,

    /// <summary>
    /// An informational message.
    /// </summary>
    Message,

    /// <summary>
    /// A warning message.
    /// </summary>
    Warning,

    /// <summary>
    /// An error message.
    /// </summary>
    Error
}
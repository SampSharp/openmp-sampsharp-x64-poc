namespace SampSharp.OpenMp.Core.Api;

/// <summary>
/// Specifies the slots available for vehicle components.
/// </summary>
public enum VehicleComponentSlot
{
    /// <summary>
    /// No component slot.
    /// </summary>
    None = -1,

    /// <summary>
    /// Slot for a spoiler component.
    /// </summary>
    Spoiler = 0,

    /// <summary>
    /// Slot for a hood component.
    /// </summary>
    Hood = 1,

    /// <summary>
    /// Slot for a roof component.
    /// </summary>
    Roof = 2,

    /// <summary>
    /// Slot for a side skirt component.
    /// </summary>
    SideSkirt = 3,

    /// <summary>
    /// Slot for lamps.
    /// </summary>
    Lamps = 4,

    /// <summary>
    /// Slot for nitro.
    /// </summary>
    Nitro = 5,

    /// <summary>
    /// Slot for an exhaust component.
    /// </summary>
    Exhaust = 6,

    /// <summary>
    /// Slot for wheels.
    /// </summary>
    Wheels = 7,

    /// <summary>
    /// Slot for a stereo component.
    /// </summary>
    Stereo = 8,

    /// <summary>
    /// Slot for hydraulics.
    /// </summary>
    Hydraulics = 9,

    /// <summary>
    /// Slot for a front bumper component.
    /// </summary>
    FrontBumper = 10,

    /// <summary>
    /// Slot for a rear bumper component.
    /// </summary>
    RearBumper = 11,

    /// <summary>
    /// Slot for a right vent component.
    /// </summary>
    VentRight = 12,

    /// <summary>
    /// Slot for a left vent component.
    /// </summary>
    VentLeft = 13,

    /// <summary>
    /// Slot for a front bullbar component.
    /// </summary>
    FrontBullbar = 14,

    /// <summary>
    /// Slot for a rear bullbar component.
    /// </summary>
    RearBullbar = 15
}